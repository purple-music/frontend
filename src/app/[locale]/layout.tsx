import { ReactQueryDevtools } from "@tanstack/react-query-devtools";
import type { Metadata } from "next";
import React from "react";
import { Toaster } from "sonner";

import { initializeStudios } from "@/actions/mutation/initialize-studios";
import Providers from "@/app/Providers";
import initTranslations from "@/app/i18n";
import TranslationProvider from "@/components/shared/providers/TranslationsProvider";

import "./tailwind.css";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const i18nNamespaces = ["index", "common", "auth", "my"];

export default async function RootLayout({
  children,
  params,
}: Readonly<{
  children: React.ReactNode;
  params: Promise<{ locale: string }>;
}>) {
  const awaitedParams = await params;
  const locale = awaitedParams.locale;

  await initializeStudios();

  const { t, resources } = await initTranslations(locale, i18nNamespaces);

  return (
    <html lang={locale}>
      <body>
        <Providers>
          <TranslationProvider
            resources={resources}
            locale={locale}
            namespaces={i18nNamespaces}
          >
            {children}
            <Toaster closeButton />
          </TranslationProvider>
          <ReactQueryDevtools initialIsOpen={false} />
        </Providers>
      </body>
    </html>
  );
}
